    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        areaFiguras rectA = new areaFiguras();//LLamando la libreria mediante un objeto
        String tArea = txtBase.getText().trim();
        String tAr = txtAltura.getText().trim();
        
        if (tArea.isEmpty() && tAr.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe Ingresar Un Valor","Error",0);
        }{
            if (!tArea.isEmpty() && !tAr.isEmpty()) {
                double pe = Double.parseDouble(tArea);
                double pd = Double.parseDouble(tAr);
                double pg = (rectA.areaRectangulo(pe, pd));// Lammando el metodo Area Triangulo
                String ps = String.valueOf(pg);
                txtResultado.setText(ps);
            }
            if (tArea.isEmpty() && tAr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Esta Vacio");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

